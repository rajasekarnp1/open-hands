# Function to create system monitoring service
function Create-MonitoringService {
    param([string]$Path)
    
    Write-Host "Creating system monitoring service..." -ForegroundColor Yellow
    
    $monitoringScript = @"
import psutil
import time
import json
import requests
from datetime import datetime

class SystemMonitor:
    def __init__(self):
        self.api_url = "http://localhost:8000/api/v1/metrics"
        self.running = True
    
    def get_system_metrics(self):
        cpu_percent = psutil.cpu_percent(interval=1)
        memory = psutil.virtual_memory()
        disk = psutil.disk_usage('/')
        
        # Get GPU info if available
        gpu_info = None
        try:
            import GPUtil
            gpus = GPUtil.getGPUs()
            if gpus:
                gpu = gpus[0]
                gpu_info = {
                    'name': gpu.name,
                    'load': gpu.load * 100,
                    'memory_used': gpu.memoryUsed,
                    'memory_total': gpu.memoryTotal,
                    'temperature': gpu.temperature
                }
        except ImportError:
            pass
        
        # Get temperature
        temperature = None
        try:
            temps = psutil.sensors_temperatures()
            if temps:
                for name, entries in temps.items():
                    if entries:
                        temperature = entries[0].current
                        break
        except:
            temperature = 0
        
        return {
            'timestamp': datetime.now().isoformat(),
            'cpu': {
                'percent': cpu_percent,
                'count': psutil.cpu_count(),
                'freq': psutil.cpu_freq()._asdict() if psutil.cpu_freq() else None
            },
            'memory': {
                'total': memory.total,
                'available': memory.available,
                'percent': memory.percent,
                'used': memory.used
            },
            'disk': {
                'total': disk.total,
                'used': disk.used,
                'free': disk.free,
                'percent': (disk.used / disk.total) * 100
            },
            'gpu': gpu_info,
            'temperature': temperature,
            'uptime': time.time() - psutil.boot_time()
        }
    
    def send_metrics(self, metrics):
        try:
            response = requests.post(self.api_url, json=metrics, timeout=5)
            return response.status_code == 200
        except:
            return False
    
    def run(self):
        while self.running:
            metrics = self.get_system_metrics()
            self.send_metrics(metrics)
            time.sleep(2)  # Update every 2 seconds

if __name__ == "__main__":
    monitor = SystemMonitor()
    monitor.run()
"@

    $monitoringScript | Out-File -FilePath "$Path\system_monitor.py" -Encoding UTF8
}

